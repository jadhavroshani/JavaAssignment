package basicsOfJava;

public class variables 
{
	public static void main(String[]agrs)
	{
		//String
		System.out.println("Stores text,such as " + "Hello" +"String values are surrounded by double quotes" );
		String name="Roshani";
		System.out.println("name");
		
		//int
		System.out.println("Stores integers,without decimals,such as 123 or -123");
		int num=2345;
		System.out.println(num);
		
		//float
		System.out.println("stores floating point numbers, with decimals,such as 99.99 or-99.99");
		float ft=5.6f;
		System.out.println(ft);
		
		//char
		System.out.println("stores single characters,such as 'a' or 'B'.Char values are surrounded by single quotes ");
		char ch='c';
		System.out.println(ch);
		
		//boolean
		System.out.println("stores values with two states: true or false");
		boolean myBool=true;
		System.out.println(myBool);

		
		
	}

}
