package ContronFlowStatement;

public class ifelse {
	public static void main(String[] args) {
	    int time = 12;
	    if (time < 13) {
	      System.out.println("Good morning.");
	    } else if (time < 20) {
	      System.out.println("Good day.");
	    }  else {
	      System.out.println("Good evening.");
	    }
	  }

}
